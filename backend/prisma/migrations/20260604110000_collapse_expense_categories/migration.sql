    'SECURITY_CONSULTING', 'RISK_ASSESSMENT', 'SECURITY_AUDIT', 'GUARD_TRAINING',
    'DONATIONS', 'CHARITY', 'GIFTS', 'PETTY_CASH', 'CONTINGENCY', 'MISCELLANEOUS', 'OTHER'
  ) THEN 'CONSULTANCY'
  WHEN "expenseCategory" IN (
    'ADVERTISEMENT_AND_SIGNAGE', 'ADVERTISING', 'DIGITAL_MARKETING', 'SOCIAL_MEDIA_ADS',
    'PRINT_ADS', 'BILLBOARDS', 'RADIO_ADS', 'TV_ADS', 'PROMOTIONS', 'DISCOUNTS',
    'WEBSITE', 'SEO', 'CONTENT_CREATION', 'BRANDING', 'EVENT_SPONSORSHIP', 'TRADE_SHOWS',
    'MARKETING_MATERIALS', 'BROCHURES', 'FLYERS', 'BUSINESS_CARDS'
