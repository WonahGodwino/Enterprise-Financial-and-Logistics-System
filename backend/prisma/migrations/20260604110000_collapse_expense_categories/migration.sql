  WHEN "expenseCategory" IN ('TRANSPORT_AND_TRAVELING', 'PARKING', 'DRIVER_ALLOWANCE', 'LOCAL_TRAVEL', 'INTERNATIONAL_TRAVEL', 'TRANSPORTATION', 'TAXI', 'RENTAL_CAR', 'FUEL_TRAVEL', 'PARKING_TRAVEL') THEN 'TRANSPORT_AND_TRAVELING'
